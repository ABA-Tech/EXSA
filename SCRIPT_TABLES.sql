-- ============================================================
-- TRIGGER : mise ŕ jour automatique de STOCK_ACTUEL
-- aprčs chaque mouvement (entrée/sortie)
-- ============================================================

CREATE OR ALTER TRIGGER TRG_MOUVEMENT_STOCK_UPDATE_STOCK
ON MOUVEMENT_STOCK AFTER INSERT AS
BEGIN
    SET NOCOUNT ON;

    -- Entrées : on additionne
    UPDATE ARTICLE_STOCK
    SET STOCK_ACTUEL = STOCK_ACTUEL + i.QUANTITE,
        DATE_MODIFICATION = SYSDATETIME()
    FROM ARTICLE_STOCK a
    INNER JOIN inserted i ON a.ID_ARTICLE = i.ID_ARTICLE
    WHERE i.TYPE_MOUVEMENT = 'ENTREE';

    -- Sorties : on soustrait (avec vérification anti-négatif)
    UPDATE ARTICLE_STOCK
    SET STOCK_ACTUEL = STOCK_ACTUEL - i.QUANTITE,
        DATE_MODIFICATION = SYSDATETIME()
    FROM ARTICLE_STOCK a
    INNER JOIN inserted i ON a.ID_ARTICLE = i.ID_ARTICLE
    WHERE i.TYPE_MOUVEMENT = 'SORTIE'
      AND a.STOCK_ACTUEL >= i.QUANTITE;

    -- Erreur si stock insuffisant pour une sortie
    IF EXISTS (
        SELECT 1
        FROM ARTICLE_STOCK a
        INNER JOIN inserted i ON a.ID_ARTICLE = i.ID_ARTICLE
        WHERE i.TYPE_MOUVEMENT = 'SORTIE'
          AND a.STOCK_ACTUEL < i.QUANTITE
    )
    BEGIN
        RAISERROR('Stock insuffisant pour effectuer cette sortie.', 16, 1);
        ROLLBACK TRANSACTION;
    END
END;
GO

