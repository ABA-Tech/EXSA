    SELECT
        a.ID_ARTICLE,
        a.ID_LOCATAIRE,
        l.NOM           AS NOM_LOCATAIRE,
        a.NOM           AS NOM_ARTICLE,
        a.REFERENCE,
        a.UNITE,
        a.STOCK_ACTUEL,
        a.STOCK_MINIMUM,
        a.PRIX_UNITAIRE_XAF
    FROM ARTICLE_STOCK a
    INNER JOIN LOCATAIRE l ON l.ID_LOCATAIRE = a.ID_LOCATAIRE
    WHERE a.STOCK_ACTUEL <= a.STOCK_MINIMUM;
GO

-- Vue : factures en retard
CREATE OR ALTER VIEW VUE_FACTURES_EN_RETARD AS
    SELECT
        f.ID_FACTURE,
        f.ID_LOCATAIRE,
        l.NOM           AS NOM_LOCATAIRE,
        f.REFERENCE,
        f.NOM_CLIENT,
        f.TOTAL_XAF,
        f.DATE_ECHEANCE,
        DATEDIFF(DAY, f.DATE_ECHEANCE, CAST(SYSDATETIME() AS DATE)) AS JOURS_RETARD
    FROM FACTURE f
    INNER JOIN LOCATAIRE l ON l.ID_LOCATAIRE = f.ID_LOCATAIRE
    WHERE f.STATUT NOT IN ('PAYEE', 'BROUILLON')
      AND f.DATE_ECHEANCE < CAST(SYSDATETIME() AS DATE);
GO


