        c.NOM_COMPLET   AS NOM_CREATEUR
    FROM INTERVENTION i
    LEFT JOIN AFFECTATION_INTERVENTION a
        ON a.ID_INTERVENTION = i.ID_INTERVENTION AND a.EST_PRINCIPAL = 1
    LEFT JOIN UTILISATEUR u ON u.ID_UTILISATEUR = a.ID_TECHNICIEN
    LEFT JOIN UTILISATEUR c ON c.ID_UTILISATEUR = i.ID_CREATEUR
    WHERE i.EST_SUPPRIME = 0;
GO

